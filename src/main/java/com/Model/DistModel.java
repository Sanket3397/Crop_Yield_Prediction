package com.Model;
public class DistModel 
{
	private int distId;
	private String distName;
	
	public DistModel()
	{
		
	}
	public int getDistId() {
		return distId;
	}
	public void setDistId(int distId) {
		this.distId = distId;
	}
	public String getDistName() {
		return distName;
	}
	public void setDistName(String distName) {
		this.distName = distName;
	}
}
